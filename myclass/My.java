class D{
	public static void main(String []args){
		  System.out.print("Star pattern");

		for(int j=1;j<=5;j++){
		for(int i=1;i<=j;i++){
			System.out.print('*');
			}
			System.out.println();
		}
		   System.out.print("Second Star pattern");

		for(int a=5;a>=1;a--){
			for(int b=1;b<=a;b++){
		System.out.print('*');

			}
		System.out.println();
		}
		

		}
	}
