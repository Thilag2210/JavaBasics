import java.util.Scanner;
class A
{
	public static void main(String[] args){
		Scanner sc=new Scanner(System.in);
	
	 System.out.println("Enter the number");
	 int a=sc.nextInt();
	 for( int i=0;i<=a;i++){
		 for(int j=0;i>=j;j++){
			 	 System.out.print(" ");
		 }
		 for(int k=a;k>=i;k--){
			 	 System.out.print("*");
		 }
		 	 System.out.println();
		 
		 
		 
		 
	 } 
		 
	}}